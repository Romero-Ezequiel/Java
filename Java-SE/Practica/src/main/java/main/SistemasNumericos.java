package main;

public class SistemasNumericos {

    //Declaro un valor decimal
    int numeroDecimal = 10;

    //Declaro un valor octal
    int numeroOctal = 012;

    //Declaro un valor Hexadecimal
    int numeroHexadecimal = 0xA;

    //Declaro un valor binario
    int numeroBinario = 0b1010;

    
    public void Imprimir(){
        System.out.println("numeroDecimal = " + this.numeroDecimal);
        System.out.println("numeroOctal = " + this.numeroOctal);
        System.out.println("numeroHexadecimal = " + this.numeroHexadecimal);
        System.out.println("numeroBinario = " + this.numeroBinario);
        
    }
    
}
